<p>Примечание: любое из трех вышеперечисленных исполнений может быть дополнительно снабжено ЭЛТ-, TFT-дисплеем, а также промышленной клавиатурой любой степени защиты. Эта возможность может быть очень полезна для организации редактирования программ на станках с ЧПУ, не имеющих встроенных средств корректирования информации.
<p>Оснащение дисплеем и клавиатурой одной из систем ЧПУ участка станков, объединенных в сеть, позволяет организовать диспетчерское место по распределению и корректировке технологических программ данного участка. 


<div class="h2">Назначение:</div>

<p>Расширение эксплуатационных возможностей станков с устаревшими (2С42-61, 2С42-65, 2Р-32, Н22, Н33, Луч, Размер, Алмаз, Bosch Micro 8 и др.) и современными (<a href="prod.10.03.php">Fagor</a>, Sinumerik) системами ЧПУ.
<p>Расширение достигается за счет внешнего добавления к системам ЧПУ отсутствующих у них аппаратных возможностей ввода-вывода информации и подключения станков к единой цеховой сети.
 
<div class="h2">Конструкция:</div>

<p>Устройства представляют собой собранные из готовых узлов РС-совместимые контроллеры, которые конфигурируются под конкретную систему ЧПУ. Использование в конструкции только стандартных и широкодоступных комплектующих изделий гарантирует высокую степень ремонтопригодности. Базовое математическое обеспечение, операционная система и предназначенные для обмена с системой ЧПУ файлы хранятся в энергонезависимой Flash-памяти объемом не менее 8 МБ.

<div class="h2">Преимущества:</div>

<ul>
	<p><li>возможность оснащения любой системы ЧПУ 3,5" дисководом, а также USB-портом для подключения миниатюрных накопителей USB Flash Drive;</li>
	<p><li>возможность объединения любых станков в сеть;</li>
	<p><li>возможность оснащения любой системы ЧПУ дополнительным объемом памяти (до 256 МБ);</li>
	<p><li>возможность работы со всеми основными командами файловой системы MS-DOS и полный доступ с панели оператора к любому месту заводской сети;</li>
	<p><li>возможность работать как в сетевом, так и в автономном режиме без дополнительной переналадки.</li>
</ul>

<div class="h2">Для каждого из типов систем ЧПУ устройства могут поставляться в различных конструктивных исполнениях:</div>


<p><b>Панельное исполнение — самое простое и экономичное</b>
<p>Предназначено для установки в отсеке фотосчитывающего устройства (для старых систем ЧПУ) или в станочном электрошкафу со степенью защиты IP43. В базовой конфигурации имеет на панели стандартный 3,5" дисковод и кнопку включения. По заказу возможно оснащение и другими интерфейсами.
<p>Поскольку все управление устройством осуществляется с панели оператора системы ЧПУ, отсек с ним может быть закрыт на замок для предотвращения возможности несанкционированного доступа.

<p><b>Корпусное исполнение — компактный вариант</b>
<p>Компактность достигается благодаря использованию аппаратной базы стандарта РС/104.
<p>Исполнение предназначено для установки либо в электрошкафу, либо в станочном пульте рядом с панелью системы ЧПУ. Имеет габаритные размеры 120 х 140 х 175 мм и массу 1,8 кг. По требованию заказчика может иметь следующие интерфейсные разъемы: для подключения дисковода 3,5", RS-232, PS/2, LAN Ethernet, VGA. Дисковод 3,5" в этом случае располагается отдельно.

<p><b>Переносное исполнение</b>
<p>Малогабаритный корпусной вариант со встроенным дисководом 3,5", разъемом для подключения к системе ЧПУ и ручкой для переноски.
<p>Предназначено для использования в качестве удобного средства загрузки и выгрузки технологических программ в станках, не оснащенных стационарными устройствами бесперфоленточного ввода-вывода. Программы хранятся во встроенной Flash-памяти объемом не менее 8 МБ. Подключение к станкам осуществляется с помощью кабеля по последовательному (RS-232) или параллельному (LPT) интерфейсу.


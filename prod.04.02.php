	<li>модульного преобразователя (или преобразователей) с зависящими от применения платами согласования и общепромышленным набором интерфейсов;</li>
	<li>синхронных и асинхронных электродвигателей нового поколения для приведения в движение любых рабочих органов станка, включая привод главного движения.</li>
</ul>
<p><br><b>В состав приводов Diax04 может входить два различных вида модульных блоков питания:</b>
<ul>

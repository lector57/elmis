	<li>блоки питания типа HVR02&nbsp;&#0151; HVR03 с поддерживаемой мощностью до 25 или до 45&nbsp;кВт, имеющие режим рекуперации энергии (возврата электроэнергии в сеть) соответственно с мощностью рекуперации до 15 или до 30&nbsp;кВт.</li>
</ul>

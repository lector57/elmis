<li>
<div class="h2">Модульная версия (Modular System)</div>
<p>Комбинированная система из нескольких управляющих модулей, соединенных с общим блоком питания шиной постоянного тока. Позволяет использовать различные приводные модули для бесщеточных (синхронных) и индукционных (асинхронных) серводвигателей с подачей к ним энергии от одного блока питания. Модули для управления синхронными электродвигателями (подач по осям) имеют марку AXD, а для управления асинхронными электродвигателями (шпинделей) — марку SPD.

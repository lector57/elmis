Система состоит из двух версий полностью цифровых приводов, в которых внутренняя обработка сигналов является полностью цифровой с использованием технологии векторного управления и поддержкой всех трех контуров регулирования (по положению, скорости и моменту). Обе версии могут напрямую подсоединяться к сети переменного тока напряжением 400-460 В:


<p><li>
<div class="h2">Компактная версия (Compact Drive)</div>
<p>Hезависимый цифровой привод со встроенным блоком питания. Способен управлять двигателем полностью автономно, без необходимости использования дополнительных модулей, что сокращает число кабельных соединений и дает возможность подключать привод непосредственно к цеховой питающей сети.
<p>Имеются варианты компактной версии для управления осями (синхронные электродвигатели, преобразователи серии AСD) и для управления шпинделями (асинхронные электродвигатели, преобразователи серии SСD).

<div class="h2">Возможный набор интерфейсов приводов семейства Fagor Digital Servo Drive System:</div>
<ul>
	<p><li><b>Плата датчика обратной связи</b> - для прямого подключения устройства отслеживания положения оси (преобразователя линейных или круговых перемещений).</li>
	<p><li><b>Плата эмулятора датчика обратной связи </b>- она выводит количество импульсов на оборот двигателя, которое может быть установлено параметрически (любое значение в диапазоне от 1 до 16384 импульсов на оборот, программируемая метка ссылки (Io), дифференциальный TTL).</li>
	<p><li><b>Плата датчика двигателя </b>- для считывания сигналов от резольвера или энкодера, закрепленного на двигателе и передающего информацию о точном положении ротора и его скорости.</li>
	<p><li><b>Плата интерфейса RS-232 C</b> - для соединения с персональным компьютером или программирующим модулем для установки параметров и наблюдения за работой системы (от 9600 до 19200 бод).</li>
	<p><li><b>Плата цифрового интерфейса SERCOS </b>- для передачи команд положения, скорости и момента по оптоволоконному кабелю, полностью свободному от электромагнитных влияний извне.</li>
	<p><li><b>Плата аналогового интерфейса </b>- содержит 2 аналоговых входа ± 10 В и 2 аналоговых выхода для передачи системе ЧПУ команд скорости и отслеживания внутренних параметров соответственно. На ней также расположены 4 цифровых входа и 4 цифровых выхода, все они полностью программируемые.</li>
	<p><li><b>Цифровые платы входов/выходов</b>:  D1 - с 16 полностью программируемыми цифровыми входами и 8 выходами; D2 - Плата с 8 полностью программируемыми цифровыми входами и 16 выходами. </li>
</ul>

<div class="h2">Дополнительные модули цифровых приводов Fagor Digital Servo Drive System:</div>
<ul>
<p>	<li><b>Резисторный модуль</b> - для рассеивания энергии, возвращаемой двигателями во время торможения, когда ее величина превышает возможности встроенного резистора. Доступны сопротивления от 18 до 43 Ом для мощностей от 300 до 1480 Вт.</li>
<p>	<li><b>Модуль сетевого фильтра EMK </b>- для обеспечения требований директивы Европейского Союза № 89/336/СЕ по электромагнитной совместимости. FAGOR поставляет сетевые фильтры EMK для диапазона токов от 40 до 120 А.</li>
<p>	<li><b>Конденсаторный модуль СМ-60</b> - для сохранения энергии, возвращаемой двигателями во время торможения. Емкость конденсаторного модуля - 4 mF.</li>
<p>	<li><b>Дроссели к блокам питания серии XPS </b>- для ограничения пиковых значений возвращаемого в сеть регенерируемого тока в связи с ее небольшим полным сопротивлением.</li>
<p>	<li><b>Модуль программирования </b>- заменяет персональный компьютер в процессе установки параметров и наблюдения за работой системы. Может сохранять таблицы параметров и предлагает различные монтажные возможности.</li>
</ul>



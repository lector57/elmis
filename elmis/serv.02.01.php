	<li>Диагностика состояния датчиков положения. Замена ненадежных датчиков.</li>
	<li>Контроль и профилактические работы на двигателях приводов.</li>
	<li>Диагностика, ремонт, замена плат, настройка приводов. Испытания приводов на стенде.</li>
	<li>При отсутствии приводов или невозможности их ремонта&nbsp;&#0151; замена на такие же или аналогичные.</li>
</ul>

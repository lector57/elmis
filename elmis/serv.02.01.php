<a name="b" style="color:black"><div class="H2a">Результирующее состояние станка после полного восстановления</div></a>
В результате проведенных восстановительных работ заказчик получает станок в следующем состоянии:
<ul>
	<li>Механическая часть станка соответствует нормам точности по ТУ на данный станок.</li>
	<li>Состояние изоляции электрических цепей соответствует ПЭУ.</li>
	<li>Переходное сопротивление в местах контактов минимально.</li>
	<li>Аппараты и провода маркированы.</li>
	<li>Надежная коммутация электрических цепей аппаратами.</li>
	<li>Защитная аппаратура соответствует схеме и спецификации.</li>
	<li>Бесконтактные датчики срабатывают надежно.</li>
	<li>Электродвигатели вычищены и высушены.</li>
	<li>Коллекторы прошлифованы и продорожены. Щетки заменены.</li>
	<li>Приводы отремонтированы, настроены и испытаны.</li>
	<li>Измерительные датчики проверены и выставлены.</li>
	<li>Устройство ЧПУ проверено, настроено и испытано.</li>
	<li>Станок отлажен, испытан на надежность.</li>
	<li>Станок проверен по нормам точности на тестовых деталях.</li>
	<li>Изготовлена партия деталей заказчика.</li>
	<li>Персонал обучен, документация передана в полном объеме.</li>
</ul>
Следствием этого являются:
<ul>
	<li>полное восстановление работоспособности станка;</li>
	<li>восстановление его технологических возможностей;</li>
	<li>восстановление точностных характеристик станка;</li>
	<li>снижение затрат на режущий инструмент;</li>
	<li>снижение процента брака;</li>
	<li>снижение времени простоя;</li>
	<li>повышенние безопасности работы персонала.</li>
</ul>
С учетом того, что механические части станка (если станок ранее уже эксплуатировался) к этому времени достаточно &laquo;притерлись&raquo;, а ресурс и надежность электронных и электрических частей благодаря проведенным работам восстановлены в максимально возможном объеме, срок службы станка значительно продлевается и, соответственно, увеличивается прибыль от возможности его эффективного использования в производственном процессе.


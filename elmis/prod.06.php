Разработанная специалистами компании &laquo;Элмис&raquo; плата RAMROM с программатором предназначена для замены всех плат памяти системы ЧПУ 2С42-65 (2Р22). Она значительно увеличивает общий объем памяти СЧПУ, дает возможность гибко конфигурировать структуру памяти и, при наличии в системе ЧПУ соответствующего ПО, обеспечивает сохранение информации в памяти при выключении питания.
<p>Имеется возможность организации 7 банков памяти, из них 2 любых банка могут быть со страничной организацией до 8 страниц. Для любого банка и любой страницы банка со страничной организацией может быть назначен тип памяти как RAM, так и EPROM.
<p>На плате имеются аппаратные средства, позволяющие программировать основной EPROM.

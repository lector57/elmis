<div class="h2">Общее описание</div>
Плата RAMROM работает с любыми исполнениями стойки 2С42-65 и требует минимальных аппаратных доработок. Плата RAMROM устанавливается в корзину процессора стойки 2С42-65.
<p>ОЗУ платы выполнено на двух микросхемах статического ОЗУ 128К&nbsp;<font face="Symbol">&#0180;</font>&nbsp;8. Для сохранения информации при выключении питания стойки на плате установлена литиевая батарея 3,6&nbsp;В, емкость которой обеспечивает хранение данных в ОЗУ в течение 5 лет. Встроенный супервизор питания осуществляет переключение питания микросхем ОЗУ с +&nbsp;5&nbsp;В на батарею, запрет обращения к ОЗУ при батарейном питании, управление сигналом Reset и управление индикатором разрядки батареи.
<p> ПЗУ платы выполнено на двух микросхемах EPROM с ультрафиолетовым стиранием 128К&nbsp;<font face="Symbol">&#0180;</font>&nbsp;8. На плате имеются аппаратные средства, позволяющие программировать основной EPROM. Для записи EPROM существует программа, реализующая быстрый интеллектуальный алгоритм Intel. Программирование платы RAMROM можно осуществить и на внешнем программаторе, поскольку микросхемы установлены на панелях.
<p>Конфигурирование структуры памяти осуществляется с помощью двух диспетчеров адреса с селекторами страниц, расположенных на плате. Они заменяют диспетчеры памяти стойки (субблок SB075 или SB231 в зависимости от исполнения).
